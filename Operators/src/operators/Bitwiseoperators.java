package operators;

public class Bitwiseoperators {

	public static void main(String[] args) {
		
		        int a = 12; // Binary: 1100
		        int b = 5;  // Binary: 0101
		        System.out.println("a = " + a + ", b = " + b);
		        // AND
		        System.out.println("a & b = " + (a & b)); 
		        // OR
		        System.out.println("a | b = " + (a | b)); 
		        //  XOR
		        System.out.println("a ^ b = " + (a ^ b)); 
		        //  Complement
		        System.out.println("~a = " + (~a)); 
		        System.out.println("~b = " + (~b)); 
		    }
		

		

	}


